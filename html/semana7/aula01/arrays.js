let lista = [];

lista[0] = 'leite';
lista[1] = 'carne';
lista[2] = 'frango';
lista[3] = 'maçã';
lista[4] = 'abacate';
lista[5] = 'peixe';

// Adiciona um elemento no final da lista
lista.push('uva');

// Tira o último elemento da lista
lista.pop();

// Adiciona um elemento no inicio da lista
lista.unshift('aveia');

// Tira o primeiro elemento da lista
lista.shift();

// Pode Adicionar ou retirar elementos de uma lista
lista.splice();

console.log(lista);
